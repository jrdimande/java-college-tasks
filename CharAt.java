public class CharAt{
    public static void main(String[] args) {
        
        String name = "teddy";
   
        System.out.println("Length: " + name.length()); // return the length of the string
        System.out.println(name.charAt(0)); // return t
        System.out.println(name.charAt(1)); // return e
        System.out.println(name.charAt(2)); // return d
        System.out.println(name.charAt(3)); // return d
        System.out.println(name.charAt(4)); // return y
         
    }
}