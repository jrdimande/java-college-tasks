        // Variaveis do tipo inteiro
        int num1 = 10;
        int num2 = 15;

        // Variáveis do tipo double
        double num3 = 10.25;
        double num4 = 20.75;

        // Variáveis do tipo character
        char letra1 = 'A';
        char letra2 = 'B';

        // Variáveis do tipo boolean
        boolean verdadeiro = true;
        boolean falso = false;

        // Imprimir os valores das váriaveis
        System.out.println("Valores das variáveis do tipo inteiro:");
        System.out.println("num1: "+num1);
        System.out.println("num2: "+num2);

        System.out.println("Valores das variáveis do tipo double: ");
        System.out.println("num3: "+num3);
        System.out.println("num4: "+num3);

        System.out.println("Valor das variáveis do tipo character: ");
        System.out.println("letra1: "+letra1);



